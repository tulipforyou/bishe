package com.sch.model;

public class grade1 {
	private int grade1;
	private int grade2;
	private int grade3;
	public grade1() {
		super();
	}
	public grade1(int grade1, int grade2, int grade3) {
		super();
		this.grade1 = grade1;
		this.grade2 = grade2;
		this.grade3 = grade3;
	}
	public int getGrade1() {
		return grade1;
	}
	public void setGrade1(int grade1) {
		this.grade1 = grade1;
	}
	public int getGrade2() {
		return grade2;
	}
	public void setGrade2(int grade2) {
		this.grade2 = grade2;
	}
	public int getGrade3() {
		return grade3;
	}
	public void setGrade3(int grade3) {
		this.grade3 = grade3;
	}
	
}
