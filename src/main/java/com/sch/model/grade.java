package com.sch.model;

public class grade {
	private int no;
	private int grade;
	public grade() {
		super();
	}
	public grade(int no, int grade) {
		super();
		this.no = no;
		this.grade = grade;
	}
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public int getGrade() {
		return grade;
	}
	public void setGrade(int grade) {
		this.grade = grade;
	}
	
}
